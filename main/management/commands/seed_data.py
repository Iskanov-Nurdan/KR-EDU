def make_image(text, bg_color, size=(800, 500)):
    img = Image.new('RGB', size, color=bg_color)
    draw = ImageDraw.Draw(img)
    cx, cy = size[0] // 2, size[1] // 2
    arm, thick = 60, 20
    draw.rectangle([cx - thick, cy - arm, cx + thick, cy + arm], fill=(255, 255, 255, 40))
    draw.rectangle([cx - arm, cy - thick, cx + arm, cy + thick], fill=(255, 255, 255, 40))
    try:
        font = ImageFont.truetype("arial.ttf", 28)
    except Exception:
        font = ImageFont.load_default()
    bbox = draw.textbbox((0, 0), text, font=font)
    tw, th = bbox[2] - bbox[0], bbox[3] - bbox[1]
    draw.text(((size[0] - tw) // 2, (size[1] - th) // 2), text, fill=(255, 255, 255), font=font)
    buf = io.BytesIO()
    img.save(buf, format='JPEG', quality=85)
    buf.seek(0)
    return buf.read()


def make_logo(size=200):
    img = Image.new('RGBA', (size, size), (0, 0, 0, 0))
    draw = ImageDraw.Draw(img)
    navy, blue, white, gold = (26, 58, 92), (75, 175, 214), (255, 255, 255), (201, 162, 39)
    draw.ellipse([2, 2, size - 2, size - 2], fill=navy, outline=blue, width=4)
    m = size // 10
    draw.ellipse([m, m, size - m, size - m], fill=None, outline=white, width=2)
    cx, cy = size // 2, size // 2
    arm = size // 6
    thick = size // 18
    draw.rectangle([cx - thick, cy - arm, cx + thick, cy + arm], fill=white)
    draw.rectangle([cx - arm, cy - thick, cx + arm, cy + thick], fill=white)
    for angle in [0, 90, 180, 270]:
        rad = math.radians(angle)
        r = size // 2 - m - 6
        x = int(cx + r * math.cos(rad))
        y = int(cy + r * math.sin(rad))
        draw.ellipse([x - 4, y - 4, x + 4, y + 4], fill=gold)
    try:
        font = ImageFont.truetype("arial.ttf", size // 8)
    except Exception:
        font = ImageFont.load_default()
    label = "КА"
    bbox = draw.textbbox((0, 0), label, font=font)
    tw = bbox[2] - bbox[0]
    draw.text((cx - tw // 2, cy + arm + 6), label, fill=gold, font=font)
    buf = io.BytesIO()
    img.save(buf, format='PNG')
    buf.seek(0)
    return buf.read()


